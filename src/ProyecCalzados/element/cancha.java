package ProyecCalzados.element;

public class cancha {
    int cancha;
    float perro;

    public cancha(int cancha, float perro) {
        this.cancha = cancha;
        this.perro = perro;
    }

    public int getCancha() {
        return cancha;
    }

    public void setCancha(int cancha) {
        this.cancha = cancha;
    }

    public float getPerro() {
        return perro;
    }

    public void setPerro(float perro) {
        this.perro = perro;
    }
}
